# COLDATA power channels only
# see wib_power_connections.xlsx for details
# first write default 0 to all outputs
i2cset -y 2 0x22 0x4 0
i2cset -y 2 0x22 0x5 0
i2cset -y 2 0x22 0x6 0
# configure all pins as outputs
i2cset -y 2 0x22 0xc 0
i2cset -y 2 0x22 0xd 0
i2cset -y 2 0x22 0xe 0
# set all ones on COLDATA outputs
i2cset -y 2 0x22 0x4 0x6b
i2cset -y 2 0x22 0x5 0x6b
i2cset -y 2 0x22 0x6 0x6b

